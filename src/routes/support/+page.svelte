		<h2>Frequently Asked Questions (FAQ)</h2>

		<p><b>1. The game crashes or won’t open. What should I do?</b></p>

		<p>Please try the following steps:</p>

		<ul>
			<li>Restart your device</li>
			<li>Update to the latest version of the game</li>
			<li>Make sure your operating system is up to date</li>
			<li>Reinstall the app (you won’t lose your progress if your data is synced to the cloud)</li>
		</ul>

		<p><b>2. I lost my progress. Can I recover it?</b></p>
		<p>If you were connected to Game Center (iOS) or Google Play Games (Android), your progress should automatically sync. Try:</p>

		<ul>
			<li>Logging back into your account</li>
			<li>Reinstalling the game and connecting to the same account</li>
		</ul>

		<p>If your progress is still missing, please contact us with:</p>

		<ul>
			<li>Your username or player ID</li>
			<li>Device type and OS version</li>
			<li>What happened before the progress was lost</li>
		</ul>

		<p><b>3. How can I turn off notifications?</b></p>

		<p>Go to your device settings: Settings > Notifications > Tiny Boo: Homecoming > Turn Off</p>

		<p><b>4. I found a bug or glitch. How can I report it?</b></p>

		<p>Please email us with:</p>

		<ul>
			<li>A description of the bug</li>
			<li>What you were doing when it happened</li>
			<li>Screenshots or screen recordings (if possible)</li>
		</ul>

		<p><b>5. I made a purchase but didn’t receive the item.</b></p>

		<p>Sorry about that! Please:</p>

		<ul>
			<li>Wait a few minutes — sometimes the delay is temporary</li>
			<li>Restart the app</li>
			<li>Make sure your purchase was successful in your App Store/Google Play transaction history</li>
		</ul>

		<p>If it still hasn’t appeared, send us:</p>
		<ul>
			<li>A screenshot of the purchase receipt</li>
			<li>Your player ID</li>
		</ul>

		<p><b>6. Can I transfer progress to another device?</b></p>
		<p>Yes! If you are connected to Game Center or Google Play Games, your progress should sync automatically when you log in on the new device.</p>

		<p><b>7. Is there a way to delete my account or data?</b></p>
		<p>Yes — please contact us by email and we’ll guide you through the process of deleting your account and personal data, in line with GDPR and privacy policies.</p>


